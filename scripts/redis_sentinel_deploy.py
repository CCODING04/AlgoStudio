def generate_sentinel_conf(
    sentinel_port: int,
    master_name: str,
    master_ip: str,
    master_port: int,
    quorum: int,
) -> str:
    """生成 Sentinel 配置文件"""
    return SENTINEL_CONF_TEMPLATE.format(
        timestamp=datetime.now().isoformat(),
        sentinel_port=sentinel_port,
        master_name=master_name,
        master_ip=master_ip,
        master_port=master_port,
        quorum=quorum,
    )


def get_sentinel_instance_name(ip: str, port: int) -> str:
    """获取 Sentinel 实例名称"""
    return f"sentinel-{ip.replace('.', '-')}-{port}"


