async def check_redis_connection(ip: str, port: int) -> bool:
    """检查 Redis 连接"""
    try:
        import redis
        r = redis.Redis(host=ip, port=port, socket_connect_timeout=5)
        r.ping()
        return True
    except Exception:
        return False


async def configure_sentinel_on_node(
    conn: asyncssh.SSHClient,
    node: dict,
    sentinel_port: int,
    master_ip: str,
    master_port: int,
    quorum: int,
) -> dict:
    """在节点上配置 Sentinel"""
    result = {
        "node": node["ip"],
        "sentinel_port": sentinel_port,
        "success": False,
        "error": None,
    }

    try:
        # 生成配置
        conf = generate_sentinel_conf(
            sentinel_port=sentinel_port,
            master_name="algo-studio-master",
            master_ip=master_ip,
            master_port=master_port,
            quorum=quorum,
        )

        # 写入配置文件
        conf_path = f"/tmp/redis-sentinel-{sentinel_port}.conf"
        async with conn.start_sftp() as sftp:
            await sftp.write_file(conf_path, conf)

        # 检查 redis-sentinel 是否已存在
        check_cmd = f"pgrep -f 'redis-sentinel.*{sentinel_port}' || true"
        status, stdout, _ = await run_command(conn, check_cmd)

        if stdout.strip():
            result["error"] = f"Sentinel already running on port {sentinel_port}"
            return result

        # 启动 Sentinel
        start_cmd = f"redis-sentinel {conf_path} --daemonize yes --logfile /tmp/redis-sentinel-{sentinel_port}.log"
        status, stdout, stderr = await run_command(conn, start_cmd)

        if status != 0:
            result["error"] = f"Failed to start: {stderr}"
            return result

        # 验证启动
        await asyncio.sleep(2)
        if await check_sentinel_alive(conn, sentinel_port):
            result["success"] = True
        else:
            result["error"] = "Sentinel did not start properly"

    except Exception as e:
        result["error"] = str(e)

    return result


async def check_sentinel_alive(conn: asyncssh.SSHClient, port: int) -> bool:
    """检查 Sentinel 是否存活"""
    try:
        status, stdout, _ = await run_command(
            conn,
            f"pgrep -f 'redis-sentinel.*{port}'"
        )
        return bool(stdout.strip())
    except Exception:
        return False


async def verify_master_slave_replication(master_ip: str, slave_ip: str) -> dict:
    """验证主从复制状态"""
    result = {
        "master_connected": False,
        "slave_connected": False,
        "replication_ok": False,
        "details": {}
    }

    try:
        import redis

        # 检查 Master
        master = redis.Redis(host=master_ip, port=REDIS_MASTER_PORT)
        master_info = master.info("replication")
        result["master_connected"] = True
        result["details"]["master_role"] = master_info.get("role")

        # 检查 Slave
        slave = redis.Redis(host=slave_ip, port=REDIS_MASTER_PORT)
        slave_info = slave.info("replication")
        result["slave_connected"] = True
        result["details"]["slave_role"] = slave_info.get("role")
        result["details"]["slave_master_link_status"] = slave_info.get("master_link_status")

        # 验证复制状态
        if (master_info.get("role") == "master" and
            slave_info.get("role") == "slave" and
            slave_info.get("master_link_status") == "up"):
            result["replication_ok"] = True

    except Exception as e:
        result["error"] = str(e)

    return result


