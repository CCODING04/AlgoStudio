async def ssh_connect(host: str, user: str) -> asyncssh.SSHClient:
    """建立 SSH 连接"""
    return await asyncssh.connect(
        host=host,
        user=user,
        known_hosts=None,  # 内部网络
        connect_timeout=30,
    )


async def run_command(
    conn: asyncssh.SSHClient,
    command: str,
) -> tuple[int, str, str]:
    """执行远程命令"""
    result = await conn.run(command, check=False)
    return result.exit_status, result.stdout, result.stderr


