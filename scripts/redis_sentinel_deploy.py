async def deploy_redis_sentinel() -> dict:
    """部署 Redis Sentinel"""
    deployment_id = str(uuid.uuid4())[:8]
    result = {
        "deployment_id": deployment_id,
        "state": DeployState.PENDING,
        "steps": [],
        "errors": [],
        "sentinel_nodes": [],
    }

    master_ip = HEAD_NODE["ip"]
    slave_ip = WORKER_NODE["ip"]

    print(f"=== Redis Sentinel 部署开始 ===")
    print(f"部署 ID: {deployment_id}")
    print(f"Master: {master_ip}:{REDIS_MASTER_PORT}")
    print(f"Slave: {slave_ip}:{REDIS_MASTER_PORT}")
    print(f"Sentinel 端口: {SENTINEL_PORT}")
    print(f"Quorum: {QUORUM}")
    print()

    # Step 1: 检查现有 Redis 连接
    print("[Step 1] 检查 Redis 连接...")
    result["state"] = DeployState.CONFIGURING

    if not await check_redis_connection(master_ip, REDIS_MASTER_PORT):
        result["errors"].append(f"Cannot connect to Master Redis at {master_ip}:{REDIS_MASTER_PORT}")
        result["state"] = DeployState.FAILED
        return result

    if not await check_redis_connection(slave_ip, REDIS_MASTER_PORT):
        result["errors"].append(f"Cannot connect to Slave Redis at {slave_ip}:{REDIS_MASTER_PORT}")
        result["state"] = DeployState.FAILED
        return result

    print("  Redis 连接检查通过")
    result["steps"].append({"step": "check_redis", "success": True})

    # Step 2: 在 Head 节点启动 Sentinel
    print("[Step 2] 在 Head 节点启动 Sentinel...")
    result["state"] = DeployState.STARTING

    try:
        conn_head = await ssh_connect(HEAD_NODE["ip"], HEAD_NODE["user"])

        for port in HEAD_NODE["sentinel_ports"]:
            sentinel_result = await configure_sentinel_on_node(
                conn_head, HEAD_NODE, port, master_ip, REDIS_MASTER_PORT, QUORUM
            )
            result["sentinel_nodes"].append(sentinel_result)
            if sentinel_result["success"]:
                print(f"  Sentinel on {HEAD_NODE['ip']}:{port} - OK")
            else:
                print(f"  Sentinel on {HEAD_NODE['ip']}:{port} - FAILED: {sentinel_result['error']}")

        conn_head.close()
    except Exception as e:
        result["errors"].append(f"Head node SSH error: {e}")
        result["state"] = DeployState.FAILED
        return result

    # Step 3: 在 Worker 节点启动 Sentinel
    print("[Step 3] 在 Worker 节点启动 Sentinel...")

    try:
        conn_worker = await ssh_connect(WORKER_NODE["ip"], WORKER_NODE["user"])

        sentinel_result = await configure_sentinel_on_node(
            conn_worker, WORKER_NODE, SENTINEL_PORT, master_ip, REDIS_MASTER_PORT, QUORUM
        )
        result["sentinel_nodes"].append(sentinel_result)
        if sentinel_result["success"]:
            print(f"  Sentinel on {WORKER_NODE['ip']}:{SENTINEL_PORT} - OK")
        else:
            print(f"  Sentinel on {WORKER_NODE['ip']}:{SENTINEL_PORT} - FAILED: {sentinel_result['error']}")

        conn_worker.close()
    except Exception as e:
        result["errors"].append(f"Worker node SSH error: {e}")
        result["state"] = DeployState.FAILED
        return result

    # Step 4: 验证部署
    print("[Step 4] 验证部署...")
    result["state"] = DeployState.VERIFYING

    replication_result = await verify_master_slave_replication(master_ip, slave_ip)
    result["steps"].append({
        "step": "verify_replication",
        "success": replication_result["replication_ok"],
        "details": replication_result,
    })

    if replication_result["replication_ok"]:
        print("  主从复制验证通过")
    else:
        print(f"  主从复制验证失败: {replication_result}")
        result["errors"].append(f"Replication verification failed: {replication_result}")

    # 判断部署是否成功
    all_sentinels_ok = all(n["success"] for n in result["sentinel_nodes"])
    if all_sentinels_ok and replication_result["replication_ok"]:
        result["state"] = DeployState.COMPLETED
        print("\n=== 部署成功 ===")
    else:
        result["state"] = DeployState.FAILED
        print("\n=== 部署失败 ===")

    return result


